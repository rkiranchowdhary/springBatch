package domain;


public class CustomerCredit {
    private String name;
    private String credit;

    public CustomerCredit(){};

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }
}
